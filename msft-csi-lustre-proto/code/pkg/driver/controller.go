var (
	// controllerCaps represents the capability of controller service
	controllerCaps = []csi.ControllerServiceCapability_RPC_Type{
		csi.ControllerServiceCapability_RPC_CREATE_DELETE_VOLUME,
	}
)

const (
	volumeContextMDSIPAddress = "mds-ip-address"
	volumeContextFSName       = "fs-name"
	defaultSize               = 32000000000000
)

func (d *Driver) CreateVolume(ctx context.Context, req *csi.CreateVolumeRequest) (*csi.CreateVolumeResponse, error) {
	volName := req.GetName()
	if len(volName) == 0 {
		fmt.Println("CreateVolume: Volume name not provided")
		return nil, status.Error(codes.InvalidArgument, "Volume name not provided")
	}

	volumeParams := req.GetParameters()
	mdsIPAddress := volumeParams[volumeContextMDSIPAddress]
	lustreFSName := volumeParams[volumeContextFSName]

	fmt.Println("CreateVolume: ", mdsIPAddress, ", ", lustreFSName)

	return &csi.CreateVolumeResponse{
		Volume: &csi.Volume{
			VolumeId:      volName,
			CapacityBytes: defaultSize,
			VolumeContext: map[string]string{
				volumeContextMDSIPAddress: mdsIPAddress,
				volumeContextFSName:       lustreFSName,
			},
		},
	}, nil
}

